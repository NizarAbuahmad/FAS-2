export function applyTranslationOverrides(overrides: Array<{ key: string; valueEn: string; valueAr: string }>) {
  if (!Array.isArray(overrides)) return;
  overrides.forEach((o) => {
    if (!o.key) return;
    if (o.valueEn !== undefined && o.valueEn !== null) {
      (translations.en as any)[o.key] = o.valueEn;
    }
    if (o.valueAr !== undefined && o.valueAr !== null) {
      (translations.ar as any)[o.key] = o.valueAr;
    }
  });
}
